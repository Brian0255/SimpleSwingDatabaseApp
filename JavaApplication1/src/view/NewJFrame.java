    private void lookupFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lookupFieldActionPerformed
        String type = selectBox.getSelectedItem().toString();
        ArrayList<Product> matches = new ArrayList<>();
        if (!lookupField.getText().isEmpty()) {
            if (type.equals("Name")) {
                matches = DatabaseManager.searchByName(lookupField.getText());
            } else if (type.equals("Price")) {
                matches = DatabaseManager.searchByPrice(Double.parseDouble(lookupField.getText()));
            } else if (type.equals("Number")) {
                matches = DatabaseManager.searchBySku(lookupField.getText());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Field empty. Please enter something.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
        }
        DefaultListModel res = new DefaultListModel();
        for (Product p : matches) {
            res.addElement(p);
        }
        resultList.setModel(res);
    }//GEN-LAST:event_lookupFieldActionPerformed


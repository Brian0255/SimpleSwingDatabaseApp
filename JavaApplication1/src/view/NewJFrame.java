    private void newSubmitItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newSubmitItemActionPerformed
        Object[] selectedObj = resultList.getSelectedValuesList().toArray();
        ArrayList<Product> products = new ArrayList<>();
        ArrayList<Product> toAdd = DatabaseManager.getProducts();
        for (Object obj : selectedObj) {
            products.add((Product) obj);
        }
        boolean isValid = true;
        boolean canSubmit = true;
        ArrayList<JTextField> fields = new ArrayList<>();
        fields.add(newNameField);
        fields.add(newNumField);
        fields.add(newQtyField);
        fields.add(newPriceField);
        try {
            Integer.parseInt(newQtyField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "New quantity must be a number.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
            isValid = false;
        }
        try {
            Double.parseDouble(newPriceField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "New price must be a number.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
            isValid = false;
        }
        for (JTextField field : fields) {
            if (canSubmit == true) {
                canSubmit = !field.getText().isEmpty();
            }
        }
        for (Product p : products) {
            if (canSubmit) {
                if (isValid) {
                    String name = newNameField.getText();
                    String sku = newNumField.getText();
                    int qty = Integer.parseInt(newQtyField.getText());
                    double price = Double.parseDouble(newPriceField.getText());
                    Product newProduct = new Product(name, sku, qty, price);
                    toAdd.removeIf(match -> match.getName().equals(p.getName()));
                    toAdd.add(newProduct);
                    DatabaseManager.saveProducts(toAdd);
                    itemEdit.setVisible(false);
                    DefaultListModel list = (DefaultListModel) resultList.getModel();
                    list.clear();
                    JOptionPane.showMessageDialog(this, "Product was updated.", "Successful Edit", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_newSubmitItemActionPerformed


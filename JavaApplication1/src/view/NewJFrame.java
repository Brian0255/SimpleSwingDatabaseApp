    private void submitItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitItemActionPerformed
        ArrayList<JTextField> fields = new ArrayList<>();
        fields.add(nameField);
        fields.add(numField);
        fields.add(qtyField);
        fields.add(priceField);
        boolean isValid = true;
        boolean canSubmit = true;
        boolean alreadyExists = false;
        try {
            Integer.parseInt(qtyField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Quantity must be a number.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
            isValid = false;
        }
        try {
            Double.parseDouble(priceField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price must be a number.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
            isValid = false;
        }
        for (JTextField field : fields) {
            if (canSubmit == true) {
                canSubmit = true;
                if (field.getText().isEmpty()) {
                    System.out.println("empty field");
                    canSubmit = false;
                }
            }
        }
        if (canSubmit) {
            if (isValid) {
                ArrayList<Product> products = DatabaseManager.getProducts();
                for (Product p : products) {
                    if (p.getSku().equals(numField.getText())) {
                        JOptionPane.showMessageDialog(this, "Object with that code already exists.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
                        alreadyExists = true;
                    }
                }
                if (!alreadyExists) {
                    String name = nameField.getText();
                    String sku = numField.getText();
                    int qty = Integer.parseInt(qtyField.getText());
                    double price = Double.parseDouble(priceField.getText());
                    Product newProduct = new Product(name, sku, qty, price);
                    products.add(newProduct);
                    DatabaseManager.saveProducts(products);
                    JOptionPane.showMessageDialog(this, "Item was successfully added.", "Successful Action", JOptionPane.INFORMATION_MESSAGE);
                    for (JTextField field : fields) {
                        field.setText("");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "One or more fields is empty. Please fill in these fields.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitItemActionPerformed

